package bean;

public class Comments {
	String comment_id;
	String comment_text;
	String comment_user;
	String comment_write_id;
	String comment_created_data;
	String comment_update_date;
	public String getComment_id() {
		return comment_id;
	}
	public void setComment_id(String comment_id) {
		this.comment_id = comment_id;
	}
	public String getComment_text() {
		return comment_text;
	}
	public void setComment_text(String comment_text) {
		this.comment_text = comment_text;
	}
	public String getComment_user() {
		return comment_user;
	}
	public void setComment_user(String comment_user) {
		this.comment_user = comment_user;
	}
	public String getComment_write_id() {
		return comment_write_id;
	}
	public void setComment_write_id(String comment_write_id) {
		this.comment_write_id = comment_write_id;
	}
	public String getComment_created_data() {
		return comment_created_data;
	}
	public void setComment_created_data(String comment_created_data) {
		this.comment_created_data = comment_created_data;
	}
	public String getComment_update_date() {
		return comment_update_date;
	}
	public void setComment_update_date(String comment_update_date) {
		this.comment_update_date = comment_update_date;
	}



}
