package been;

public class Offices {
	String office_id;
	String office_name;
	String office_created_date;
	String office_update_date;
	public String getOffice_id() {
		return office_id;
	}
	public void setOffice_id(String office_id) {
		this.office_id = office_id;
	}
	public String getOffice_name() {
		return office_name;
	}
	public void setOffice_name(String office_name) {
		this.office_name = office_name;
	}
	public String getOffice_created_date() {
		return office_created_date;
	}
	public void setOffice_created_date(String office_created_date) {
		this.office_created_date = office_created_date;
	}
	public String getOffice_update_date() {
		return office_update_date;
	}
	public void setOffice_update_date(String office_update_date) {
		this.office_update_date = office_update_date;
	}

}
