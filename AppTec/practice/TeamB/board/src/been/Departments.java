package been;

public class Departments {
	String departments_id;
	String departments_name;
	String departments_created_date;
	String departments_update_date;
	public String getDepartments_id() {
		return departments_id;
	}
	public void setDepartments_id(String departments_id) {
		this.departments_id = departments_id;
	}
	public String getDepartments_name() {
		return departments_name;
	}
	public void setDepartments_name(String departments_name) {
		this.departments_name = departments_name;
	}
	public String getDepartments_created_date() {
		return departments_created_date;
	}
	public void setDepartments_created_date(String departments_created_date) {
		this.departments_created_date = departments_created_date;
	}
	public String getDepartments_update_date() {
		return departments_update_date;
	}
	public void setDepartments_update_date(String departments_update_date) {
		this.departments_update_date = departments_update_date;
	}

}
